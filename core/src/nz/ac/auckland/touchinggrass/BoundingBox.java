package nz.ac.auckland.touchinggrass;

public class BoundingBox {
    float x;
    float y;
    float width;
    float height;

    public BoundingBox(float x, float y, float width, float height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
}
